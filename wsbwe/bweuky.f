      SUBROUTINE BWEUKY (KEYWRD)
      IMPLICIT NONE
C----------
C  **BWEUKY                 DATE OF LAST REVISION:  07/14/10
C----------
C  THESE ROUTINES CONVERT KEYWORDS THAT ARE INPUT IN LOWER CASE LETTERS
C  TO UPPER CASE LETTERS.
C----------
      CHARACTER*8 KEYWRD
      INTEGER I

      DO 40 I=1,8
      CALL BWEUCA(KEYWRD(I:I))
   40 CONTINUE
      RETURN
      END
