      SUBROUTINE RDPPATV (L)
C----------
C  **RDPPATV          DATE OF LAST REVISION:  02/11/00
C----------
C
C  RETURNS L=TRUE, TO INDICATE THAT ROOT DISEASE MODEL IS AVAILABLE
C  TO PARALLEL PROCESSOR.
C
C  CALLED BY :
C     GETSTD  [PARALLEL PROCESSOR]
C     PUTSTD  [PARALLEL PROCESSOR]
C
C  CALLS     :
C     NONE
C
C  PARAMETERS :
C     L     - FLAG THAT IS SET TO TRUE WHEN THE ROOT DISEASE MODEL IS
C             ATTACHED WITH PPE.
C
      LOGICAL L

      L = .TRUE.

      RETURN
      END
