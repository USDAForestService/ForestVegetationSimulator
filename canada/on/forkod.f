      SUBROUTINE FORKOD
      IMPLICIT NONE
C----------
C  **FORKOD--ON DATE OF LAST REVISION:   06/02/08
C    ON: 11/20/03
C----------
C
C     TRANSLATES FOREST CODE INTO A SUBSCRIPT, IFOR, AND IF
C     KODFOR IS ZERO, THE ROUTINE RETURNS THE DEFAULT CODE.
C
C     CODES 902-913 ARE FROM THE LS VARIANT
C     CODES 915 & 916 ARE FOR ONTARIO
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'PLOT.F77'
C
C
      INCLUDE 'CONTRL.F77'
C
C
COMMONS
C
      INTEGER JFOR(11),KFOR(11),NUMFOR,I
      DATA JFOR/902,903,904,906,907,909,910,913,915,916,924/
      DATA NUMFOR/11/
      DATA KFOR/11*1/
C
      IF (KODFOR .EQ. 0) GOTO 30
      DO 10 I=1,NUMFOR
      IF (KODFOR .EQ. JFOR(I)) GOTO 20
   10 CONTINUE
      CALL ERRGRO (.TRUE.,3)
      WRITE(JOSTND,11) JFOR(IFOR)
   11 FORMAT(T13,'FOREST CODE USED FOR THIS PROJECTION IS',I4)
      GOTO 30
   20 CONTINUE
      IF(I .EQ. 11)THEN
        WRITE(JOSTND,21)
   21   FORMAT(T13,'MANISTEE NF (924) BEING MAPPED TO HURON-',
     &  'MANISTEE (904) FOR FURTHER PROCESSING.')
        I=3
      ENDIF
      IFOR=I
      IGL=KFOR(I)
   30 CONTINUE
      KODFOR=JFOR(IFOR)
C----------
C  SET DEFAULT TLAT, TLONG, AND ELEVATION VALUES, BY FOREST
C  = use US SUPERIOR for now
C----------
      SELECT CASE(KODFOR)
      CASE(915)
        IF(TLAT.EQ.0) TLAT=46.78
        IF(TLONG.EQ.0)TLONG=92.11
        IF(ELEV.EQ.0) ELEV=16.
      CASE(916)
        IF(TLAT.EQ.0) TLAT=46.78
        IF(TLONG.EQ.0)TLONG=92.11
        IF(ELEV.EQ.0) ELEV=16.
      END SELECT
      RETURN
      END
