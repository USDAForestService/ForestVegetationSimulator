c      B1=BACON*D1*D1
c      B2=BACON*D2*D2
c      CALL CCFCAL(IS,D1,HT(I),ICR(I),P,.FALSE.,C1,CW,1)
c      CALL CCFCAL(IS,D2,HT(I),ICR(I),P,.FALSE.,C2,CW,1)
c      BI=(B2-B1)/10.0
