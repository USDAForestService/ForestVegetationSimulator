c   45 CONTINUE
c      IF (ITRN.LE.0) GO TO 91
c      IF(IFINTH .EQ. 0)  GOTO 95
cC----------
cC  DETERMINE BASAL AREA IN LARGE TREES AT START OF EACH
cC  PREDICTION SUBCYCLE.
cC----------
