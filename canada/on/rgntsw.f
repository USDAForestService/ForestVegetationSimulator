c   48 CONTINUE
c   49 CONTINUE
c   50 CONTINUE
c      DO 100 ISPC=1,MAXSP
cC----------
cC  BEGIN PROCESSING TREE LIST IN SPECIES ORDER.  DO NOT CALCULATE
