      INTEGER JSP,JSPR
      REAL DBH, DIB, DOB, DBH_IB, DBT_BH,H,DIBMAX
      REAL*4 BK(8,11),B(5),CP(3),P,DR,DBT,DBTMIN,RATIO_BH
      
C     DOUGLAS FIR
      DATA (BK(I,1),I=1,8)/-0.28511, 0.006013, 0.00650,0,   2.7986,
