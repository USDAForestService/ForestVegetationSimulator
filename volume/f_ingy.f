c                                         BRK_UPD2 ROUTINE
c                                         Below-BH model
      JSPR = JSP-10
      CP(1) = BK(1,JSPR)
      CP(2) = BK(2,JSPR)
      CP(3) = BK(3,JSPR)
      B(1) = BK(4,JSPR)
      B(2) = BK(5,JSPR)
      B(3) = BK(6,JSPR)
      B(4) = BK(7,JSPR)
      B(5) = BK(8,JSPR)
      

