        RHCON(ISPC)=REGCH + RHSC(ISPC) + RHHAB(IRHHAB,ISPC)
        IF(LRCOR2.AND.RCOR2(ISPC).GT.0.0) RHCON(ISPC)=RHCON(ISPC) +
     &                                  ALOG(RCOR2(ISPC))
      ELSE
        RHCON(ISPC) = 1.0
        IF(LRCOR2 .AND. RCOR2(ISPC).GT.0.0)
     &   RHCON(ISPC)=RCOR2(ISPC)
      ENDIF
  110 CONTINUE
      RETURN
      END
