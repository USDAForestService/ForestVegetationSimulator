C----------
      NIVAR = .FALSE.
      TTVAR = .FALSE.
      UTVAR = .FALSE.
      CRVAR = .FALSE.
      IF(ISPC.LE.12 .OR. ISPC.EQ.14 .OR. ISPC.EQ.23)THEN
