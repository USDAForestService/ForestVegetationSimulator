      SUBROUTINE SITSET
      IMPLICIT NONE
C----------
C  **SITSET--NI23   DATE OF LAST REVISION:  02/25/09
C----------
C  THIS SUBROUTINE IS USED TO SET SIMULATION CONTROLLING VALUES
C  THAT HAVE NOT BEEN SET USING THE KEYWORDS --- SDIMAX, BAMAX.
C----------
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'CONTRL.F77'
C
C
      INCLUDE 'PLOT.F77'
C
C
      INCLUDE 'VOLSTD.F77'
C
C
COMMONS
C----------
C  SPECIES EXPANSION 
C  WB USE COEFFICIENTS FOR L
C  LM AND PY USE COEFFICIENTS FROM TT FOR LM
C  LL USE COEFFICIENTS FOR AF
C  AS, MM, PB USE COEFFIECIENTS FOR AS FROM UT
C  CO, OH USE COEFFIECIENTS FOR OH FROM CR
C  OS USE COEFFEICIENTS FOR MH
C  PI, JU USE COEFFICIENTS FROM UT
C----------
      CHARACTER FORST*2,DIST*2,PROD*2,VAR*2,VOLEQ*10
      INTEGER IFIASP, ERRFLAG
      REAL BAMAXA(30)
      INTEGER MAPSIT(30,MAXSP),MAPSS(30)
      INTEGER ISPC,I,J,JJ,K,INTFOR,IREGN
C
      DATA BAMAXA/
     &  140.,220.,250.,310.,240.,270.,310.,310.,200.,310.,
     &  290.,330.,380.,440.,500.,500.,390.,390.,440.,180.,
     &  290.,400.,350.,390.,260.,300.,220.,220.,160.,300./
C
      DATA MAPSIT/
     & 330*0,
     &  30*0,                                          !WB
     & 25,29,35,35,32,34,35,32,27,39,37,32,36,41,41,41,!LM
     &    43,38,39,32,36,34,34,32,36,36,36,26,22,26,
     & 30*0,                                           !LL
     &  7, 9,13,12,10,12,12,11, 8,14,11,13,16,15,15,15,!PI
     &    16,14,14,11,12,13,13,11,13,13,13, 8, 6, 8,
     &  6, 8,10,10, 9,10,10, 9, 7,11, 9,11,12,12,12,12,!JU
     &    13,11,11, 9,10,10,10, 9,10,10,10, 7, 6, 7,
     & 25,29,35,35,33,34,35,32,27,39,37,32,36,41,41,41,!PY
     &    43,38,39,32,36,34,34,32,36,36,36,26,22,26,
     & 36,43,51,50,44,49,48,46,41,55,46,52,59,58,58,58,!AS
     &    60,54,55,46,49,52,52,46,52,52,52,38,33,38,
     & 44,57,76,62,72,72,71,67,55,86,67,80,95,93,93,93,!CO
     &    98,84,86,67,73,80,80,67,80,80,80,49,36,49,
     & 36,43,51,50,44,49,48,46,41,55,46,52,59,58,58,58,!MM
     &    60,54,55,46,49,52,52,46,52,52,52,38,33,38,
     & 36,43,51,50,44,49,48,46,41,55,46,52,59,58,58,58,!PB
     &    60,54,55,46,49,52,52,46,52,52,52,38,33,38,
     & 44,57,76,62,72,72,71,67,55,86,67,80,95,93,93,93,!OH
     &    98,84,86,67,73,80,80,67,80,80,80,49,36,49,
     & 30*0/                                           !OS
C
      DATA MAPSS/2*10,7*3,2*8,2*4,3*6,5,5*9,11,9,11,3*9,12,3/
C----------
C  ASSIGN SITE INDEX BY SPECIES AND HABITAT TYPE
C----------
      IF(ISISP.EQ.0)ISISP = MAPSS(ITYPE)
      DO 5 ISPC=1,MAXSP
      IF(SITEAR(ISPC).EQ.0.)SITEAR(ISPC) = MAPSIT(ITYPE,ISPC)
    5 CONTINUE
C----------
C  SET SDIDEF AND BAMAX VALUES WHICH HAVE NOT BEEN SET BY KEYWORD.
C----------
      IF(BAMAX.LE.0) BAMAX=BAMAXA(ITYPE)
      DO 10 I=1,MAXSP
      IF(SDIDEF(I).LE.0.) SDIDEF(I)=BAMAX/(0.5454154*(PMSDIU/100.))
   10 CONTINUE
C
      DO 92 I=1,15
      J=(I-1)*10 + 1
      JJ=J+9
      IF(JJ.GT.MAXSP)JJ=MAXSP
      WRITE(JOSTND,90)(NSP(K,1)(1:2),K=J,JJ)
   90 FORMAT(/'SPECIES ',5X,10(A2,6X))
      WRITE(JOSTND,91)(SDIDEF(K),K=J,JJ )
   91 FORMAT('SDI MAX ',   10F8.0)
      IF(JJ .EQ. MAXSP)GO TO 93
   92 CONTINUE
   93 CONTINUE
C----------
C  SET METHB & METHC DEFAULTS.  DEFAULTS ARE INITIALIZED TO 999 IN
C  **GRINIT**.  IF THEY HAVE A DIFFERENT VALUE NOW, THEY WERE CHANGED
C  BY KEYWORD IN INITRE. ONLY CHANGE THOSE NOT SET BY KEYWORD.
C
C  COLVILLE  (IFOR=5) IS A REGION 6 FOREST. ALL OTHERS FORESTS IN THIS
C  VARIANT ARE IN REGION 1.
C----------
      DO 50 ISPC=1,MAXSP
      IF(IFOR.EQ.5)THEN
        IF(METHB(ISPC).EQ.999)METHB(ISPC)=6
        IF(METHC(ISPC).EQ.999)METHC(ISPC)=6
      ELSE
        IF(METHB(ISPC).EQ.999)METHB(ISPC)=6
        IF(METHC(ISPC).EQ.999)METHC(ISPC)=6
      ENDIF
   50 CONTINUE
C----------
C  LOAD VOLUME EQUATION ARRAYS FOR ALL SPECIES
C----------
      INTFOR = KODFOR - (KODFOR/100)*100
      WRITE(FORST,'(I2)')INTFOR
      IF(INTFOR.LT.10)FORST(1:1)='0'
      IREGN = KODFOR/100
      DIST='  '
      PROD='  '
      VAR='IE'
C
      DO ISPC=1,MAXSP
      READ(FIAJSP(ISPC),'(I4)')IFIASP
      IF(((METHC(ISPC).EQ.6).OR.(METHC(ISPC).EQ.9)).AND.
     &     (VEQNNC(ISPC).EQ.'          '))THEN
        CALL VOLEQDEF(VAR,IREGN,FORST,DIST,IFIASP,PROD,VOLEQ,ERRFLAG)
        VEQNNC(ISPC)=VOLEQ
      ENDIF
      IF(((METHB(ISPC).EQ.6).OR.(METHB(ISPC).EQ.9)).AND.
     &     (VEQNNB(ISPC).EQ.'          '))THEN
        CALL VOLEQDEF(VAR,IREGN,FORST,DIST,IFIASP,PROD,VOLEQ,ERRFLAG)
        VEQNNB(ISPC)=VOLEQ
      ENDIF
      ENDDO
C----------
C  IF FIA CODES WERE IN INPUT DATA, WRITE TRANSLATION TABLE
C---------
      IF(LFIA) THEN
        CALL FIAHEAD(JOSTND)
        WRITE(JOSTND,211) (NSP(I,1)(1:2),FIAJSP(I),I=1,MAXSP)
 211    FORMAT ((T12,8(A3,'=',A6,:,'; '),A,'=',A6))
      ENDIF
C----------
C  WRITE VOLUME EQUATION NUMBER TABLE
C----------
      CALL VOLEQHEAD(JOSTND)
      WRITE(JOSTND,230)(NSP(J,1)(1:2),VEQNNC(J),VEQNNB(J),J=1,MAXSP)
 230  FORMAT(4(2X,A2,4X,A10,1X,A10,1X))
C
      RETURN
      END
