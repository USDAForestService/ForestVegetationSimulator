      SUBROUTINE DGF(DIAM)
      IMPLICIT NONE
C----------
C SN $Id$
C----------
C  THIS SUBROUTINE COMPUTES THE VALUE OF DDS (CHANGE IN SQUARED
C  DIAMETER) FOR EACH TREE RECORD, AND LOADS IT INTO THE ARRAY
C  WK2.  DDS IS PREDICTED FROM SITE INDEX, LOCATION, SLOPE,
C  ASPECT, ELEVATION, DBH, CROWN RATIO, BASAL AREA IN LARGER TREES,
C  AND CCF.  THE SET OF TREE DIAMETERS TO BE USED IS PASSED AS THE
C  ARGUEMENT DIAM.  THE PROGRAM THUS HAS THE FLEXIBILITY TO
C  PROCESS DIFFERENT CALIBRATION OPTIONS.  THIS ROUTINE IS CALLED
C  BY **DGDRIV** DURING CALIBRATION AND WHILE CYCLING FOR GROWTH
C  PREDICTION.  ENTRY **DGCONS** IS CALLED BY **RCON** TO LOAD SITE
C  DEPENDENT COEFFICIENTS THAT NEED ONLY BE RESOLVED ONCE.
C----------
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'CALCOM.F77'
C
C
      INCLUDE 'ARRAYS.F77'
C
C
      INCLUDE 'COEFFS.F77'
C
C
      INCLUDE 'CONTRL.F77'
C
C
      INCLUDE 'OUTCOM.F77'
C
C
      INCLUDE 'PLOT.F77'
C
C
      INCLUDE 'PDEN.F77'
C
C
      INCLUDE 'VARCOM.F77'
C
C
COMMONS
C
C     EUT  -- ECOLOGICAL UNIT TYPE IS A USER INPUT VARIABLE IN THE
C             STAND LIST FILE (EUT=1 MOUNTAIN, EUT=0 NON MOUNTAIN)
C     DIAM -- ARRAY LOADED WITH TREE DIAMETERS (PASSED AS AN
C             ARGUEMENT).
C
C  DIMENSIONS FOR INTERNAL VARIABLES.
C
C     DBHC -- ARRAY CONTAINING COEFFICIENTS FOR THE DIAMETER TERM IN
C             THE DDS MODEL
C   INTERC -- DDS REGRESSION EQUATION INTERCEPT
C    DBHC2 -- ARRAY CONTAINING COEFFICIENTS FOR THE DIAMETER SQUARED
C             TERM IN THE DDS MODEL
C     LDBH -- ARRAY CONTAINING COEFFICIENTS FOR THE LOG(DIAMETER)
C             TERM IN THE DDS MODEL.
C     CRWN -- ARRAY CONTAINING THE COEFFICIENTS FOR THE PERCENT CROWN
C             RATIO TERM IN THE DDS MODEL.
C     CRSQ -- ARRAY CONTAINING THE COEFFICIENTS FOR THE PERCENT CROWN
C             RATIO SQUARED TERM IN THE DDS MODEL.
C     BALP -- ARRAY CONTAINING THE COEFFICIENTS FOR COMPETITION
C             RELATIVE TO TREE SIZE
C     PLTB -- ARRAY CONTAINING COEFFICIENTS FOR THE BASAL AREA IN
C             LARGER TREES TERM IN THE DDS MODEL.
C     PLTT -- ARRAY OF COEFFICIENTS OF BASAL AREA, PLOT BASIS
C     TANS -- ARRAY OF COEFFICIENTS OF TAN(SLOPE)
C     FCOS -- ARRAY OF COEFFICIENTS OF COS(ASPECT IN DEG.)
C     FSIN -- ARRAY OF COEFFICIENTS FOR SIN(ASPECT)
C
C  THE FOLOWING ARE ECOLOGICAL UNIT QUALITATIVE COEFFICIENTS
C
C    EICEC -- REGRESSION INTERCEPT MODIFIER.
C             MEANINGFULL WHEN EUT=1 (IE. MOUNTAINOUS SITE)
C   DBHCEC -- ARRAY CONTAINING COEFFICIENTS FOR THE DIAMETER TERM IN
C             THE DDS MODEL
C             MEANINGFULL WHEN EUT=1 (IE. MOUNTAINOUS SITE)
C   DBH2EC -- ARRAY CONTAINING COEFFICIENTS FOR THE DIAMETER SQUARED
C             TERM IN THE DDS MODEL
C             MEANINGFULL WHEN EUT=1 (IE. MOUNTAINOUS SITE)
C   LDBHEC -- ARRAY CONTAINING COEFFICIENTS FOR THE LOG(DIAMETER)
C             TERM IN THE DDS MODEL.
C             MEANINGFULL WHEN EUT=1 (IE. MOUNTAINOUS SITE)
C   CRWNEC -- ARRAY CONTAINING THE COEFFICIENTS FOR THE PERCENT CROWN
C             RATIO TERM IN THE DDS MODEL.
C             MEANINGFULL WHEN EUT=1 (IE. MOUNTAINOUS SITE)
C   CRSQEC --ARRAY CONTAINING THE COEFFICIENTS FOR THE PERCENT CROWN
C              RATIO SQUARED TERM IN THE DDS MODEL.
C             MEANINGFULL WHEN EUT=1 (IE. MOUNTAINOUS SITE)
C   BALPEC -- ARRAY CONTAINING THE COEFFICIENTS FOR COMPETITION
C             RELATIVE TO TREE SIZE
C             MEANINGFULL WHEN EUT=1 (IE. MOUNTAINOUS SITE)
C   PLTBEC -- ARRAY CONTAINING COEFFICIENTS FOR THE BASAL AREA IN
C             LARGER TREES TERM IN THE DDS MODEL.
C             MEANINGFULL WHEN EUT=1 (IE. MOUNTAINOUS SITE)
C   PLTTEC -- ARRAY OF COEFFICIENTS OF BASAL AREA, PLOT BASIS
C             MEANINGFULL WHEN EUT=1 (IE. MOUNTAINOUS SITE)
C   TANSEC -- ARRAY OF COEFFICIENTS OF TAN(SLOPE)
C             MEANINGFULL WHEN EUT=1 (IE. MOUNTAINOUS SITE)
C   FCOSEC -- ARRAY OF COEFFICIENTS OF COS(ASPECT IN DEG.)
C             MEANINGFULL WHEN EUT=1 (IE. MOUNTAINOUS SITE)
C   FSINEC -- ARRAY OF COEFFICIENTS FOR SIN(ASPECT)
C             MEANINGFULL WHEN EUT=1 (IE. MOUNTAINOUS SITE)
C
C----------
C  SPECIES AND COEFFICIENT ORDER
C----------
C         1=FR,   2=JU,   3=PI,   4=PU,   5=SP,   6=SA
C         7=SR,   8=LL,   9=TM,   10=PP,  11=PD,  12=WP
C         13=LP,  14=VP,  15=BY,  16=PC,  17=HM,  18=FM
C         19=BE,  20=RM,  21=SV,  22=SM,  23=BU,  24=BB
C         25=SB,  26=AH,  27=HI,  28=CA,  29=HB,  30=RD
C         31=DW,  32=PS,  33=AB,  34=AS,  35=WA,  36=BA
C         37=GA,  38=HL,  39=LB,  40=HA,  41=HY,  42=BN
C         43=WN,  44=SU,  45=YP,  46=MG,  47=CT,  48=MS
C         49=MV,  50=ML,  51=AP,  52=MB,  53=WT,  54=BG
C         55=TS,  56=HH,  57=SD,  58=RA,  59=SY,  60=CW
C         61=BT,  62=BC,  63=WO,  64=SO,  65=SK,  66=CB
C         67=TO,  68=LK,  69=OV,  70=BJ,  71=SN,  72=CK
C         73=WK,  74=CO,  75=RO,  76=QS,  77=PO,  78=BO
C         79=LO,  80=BK,  81=WI,  82=SS,  83=BW,  84=EL
C         85=WE,  86=AE,  87=RL,  88=OS,  89=OH,  90=OT
C
      REAL DIAM(MAXTRE),INTERC(MAXSP),LDBH(MAXSP),
     &   DBH2(MAXSP),LCRWN(MAXSP),HREL(MAXSP),
     &   ISIO(MAXSP),OBSERV(MAXSP),PLTB(MAXSP),
     &   PNTBL(MAXSP),TANS(MAXSP),FCOS(MAXSP),FSIN(MAXSP)
      REAL D1,D2,D3,D4,D5,D6,D7,D8,D9,D10,D11
      REAL DDS,CONSPP,D,RELHT,BAL,PBA,PBAL,BARK,BRATIO,DG5
      INTEGER KLOHD,KNOHD,KOKPN,KSFHP,KUPHD,KUPOK,KYLPN,KPLANT
      INTEGER ISPC,I1,I2,I3,I,IK
C----------
C  ECOLOGICAL UNIT CATEGORICAL VARIABLES
C----------
      REAL PM221(MAXSP),PM222(MAXSP),
     &   PM231(MAXSP),P221(MAXSP),P222(MAXSP),S231L(MAXSP),S231T(MAXSP),
     &   P232(MAXSP),P234(MAXSP),P255(MAXSP),P411(MAXSP),
     &   FTLOHD(MAXSP),FTNOHD(MAXSP),FTOKPN(MAXSP),FTSFHP(MAXSP),
     &   FTUPHD(MAXSP),FTUPOK(MAXSP),FTYLPN(MAXSP),PLANT(MAXSP)
C
      LOGICAL DEBUG
C
      INTEGER KPM221,KPM222,KPM231,KP221,KP222,KS231T,KS231L,
     & KP232,KP234,KP255,KP411
C
C-----------
C  CHECK FOR DEBUG.
C-----------
      CALL DBCHK (DEBUG,'DGF',3,ICYC)
      IF(DEBUG) WRITE(JOSTND,3)ICYC,
     & ISCT(22,1),ISCT(33,1),ISCT(90,1)
    3 FORMAT(' ENTERING SUBROUTINE DGF  CYCLE =',I5,10I5)
C----------
C  DEBUG OUTPUT: MODEL COEFFICIENTS.
C----------
      IF(DEBUG) WRITE(JOSTND,*) 'IN DGF,HTCON=',HTCON,
     *'ELEV=',ELEV,'RELDEN=',RELDEN,
     &' MANAGD= ',MANAGD
C----------
C  INITIALISZE COEFFICIENTS THAT WHICH ARE APPLIED TO THE FOREST TYPE
C  GROUP CATEGORICAL VARIABLES
C----------
      KLOHD= 0
      KNOHD= 0
      KOKPN= 0
      KSFHP= 0
      KUPHD= 0
      KUPOK= 0
      KYLPN= 0
C----------
C  SET COEF FOR MANAGED KEYWORD
C----------
      KPLANT= 0
      IF (MANAGD .GT. 0) KPLANT= 1
C----------
C  SEARCHG FOR FOREST TYPE GROUP FOR THE
C  APPLICATION OF ASSOCIATED CATEGORICAL VARIABLE
C----------
      IF ((IFORTP .EQ. 701) .OR. (IFORTP .EQ. 801) .OR.
     &    (IFORTP .EQ. 805)) THEN
C
        KNOHD= 1
C

      ELSEIF ((IFORTP .EQ. 104) .OR. (IFORTP .EQ. 105) .OR.
     &        (IFORTP .EQ. 121) .OR. (IFORTP .EQ. 124)) THEN
C
        KSFHP= 1
C
      ELSEIF ((IFORTP .EQ. 141) .OR. (IFORTP .EQ. 142) .OR.
     &        (IFORTP .EQ. 161) .OR. (IFORTP .EQ. 162) .OR.
     &        (IFORTP .EQ. 163) .OR. (IFORTP .EQ. 164) .OR.
     &        (IFORTP .EQ. 166)) THEN
C
        KYLPN= 1
C

      ELSE   IF ((IFORTP .EQ. 165) .OR. (IFORTP .EQ. 403) .OR.
     &           (IFORTP .EQ. 404) .OR. (IFORTP .EQ. 405) .OR.
     &    (IFORTP .EQ. 406) .OR. (IFORTP .EQ. 407) .OR.
     &    (IFORTP .EQ. 409)) THEN
C
        KOKPN= 1
C

      ELSE   IF ((IFORTP .EQ. 501) .OR. (IFORTP .EQ. 502) .OR.
     &           (IFORTP .EQ. 503) .OR. (IFORTP .EQ. 504) .OR.
     &    (IFORTP .EQ. 505) .OR. (IFORTP .EQ. 510) .OR.
     &    (IFORTP .EQ. 514) .OR. (IFORTP .EQ. 515)) THEN
C
        KUPOK= 1
C

      ELSEIF ((IFORTP .EQ. 168) .OR. (IFORTP .EQ. 508) .OR.
     &        (IFORTP .EQ. 601) .OR. (IFORTP .EQ. 602) .OR.
     &        (IFORTP .EQ. 605) .OR. (IFORTP .EQ. 606) .OR.
     &        (IFORTP .EQ. 607) .OR. (IFORTP .EQ. 608) .OR.
     &        (IFORTP .EQ. 702) .OR. (IFORTP .EQ. 703) .OR.
     &        (IFORTP .EQ. 704) .OR. (IFORTP .EQ. 705) .OR.
     &        (IFORTP .EQ. 706) .OR. (IFORTP .EQ. 708)) THEN
C
        KLOHD= 1
C

      ELSEIF ((IFORTP .EQ. 103) .OR. (IFORTP .EQ. 167) .OR.
     &        (IFORTP .EQ. 181) .OR. (IFORTP .EQ. 401) .OR.
     &        (IFORTP .EQ. 402) .OR. (IFORTP .EQ. 506) .OR.
     &        (IFORTP .EQ. 507) .OR. (IFORTP .EQ. 511) .OR.
     &        (IFORTP .EQ. 512) .OR. (IFORTP .EQ. 513) .OR.
     &        (IFORTP .EQ. 519) .OR. (IFORTP .EQ. 520) .OR.
     &        (IFORTP .EQ. 802) .OR. (IFORTP .EQ. 803) .OR.
     &        (IFORTP .EQ. 807) .OR. (IFORTP .EQ. 809)) THEN
C
        KUPHD= 1
C
      ENDIF

      DG5 = 0
C----------
C  BEGIN SPECIES LOOP.  ASSIGN VARIABLES WHICH ARE SPECIES
C  DEPENDENT
C----------
      DO 20 ISPC=1,MAXSP
      I1=ISCT(ISPC,1)
      IF(I1.EQ.0) GO TO 20
      I2=ISCT(ISPC,2)
      CONSPP= DGCON(ISPC) + COR(ISPC)
C----------
C  BEGIN TREE LOOP WITHIN SPECIES ISPC.
C----------
      DO 10 I3=I1,I2
      I=IND1(I3)
      D=DIAM(I)
C
      IF (D.LE.0.0) GOTO 10
C----------
C  RELATIVE HEIGHT
C----------
      RELHT = 0.0
      IF(AVH .GT. 0.0) RELHT=HT(I)/AVH
      IF(RELHT .GT. 1.5)RELHT=1.5
C----------
C  SET BA AND BAL TO NONZERO SMALL NUMBERS
C----------
      IF(BA .LE. 0.) BA= 25.
      IF(ICR(I) .LE. 0) ICR(I)= 25
      BAL = (1.0 - (PCT(I)/100.)) * BA
C
      IF(DEBUG) WRITE(JOSTND,*) 'IN DGF,D=',D,
     *'RELDEN=',RELDEN,' SI= ',
     *SITEAR(ISPC),' ISIO= ',ISIO(ISPC)
      IF(DEBUG) WRITE(JOSTND,*) 'IN DGF1,BA=',BA,
     *'ICR=',ICR(I),'SLOPE=',SLOPE,' ASPECT=',ASPECT
C----------
C  POINT BASEAL AREA LARGER
C----------
      PBA=PTBAA(ITRE(I))
      IF(PBA.LE.0.0 )PBA = BA
      PBAL=PBA*(1.0-(PCT(I)/100.))
      IF(PBAL.LE.0.0)PBAL = BAL
C----------
C  FORT BRAGG 8=LONGLEAF(121); 13=LOBLOLLY(131)
C----------
      IF(IFOR .EQ. 20)THEN
        BARK = BRATIO(ISPC,D,HT(I))
        IF(ISPC .EQ. 8)THEN
          DG5=(D*BARK)*((-0.4553*(0.09737-EXP(-0.2428*D))) 
     &        + 0.05574*(FLOAT(ICR(I))/100.) - 0.0002965*BA 
     &        - 0.00002481*PBA - 0.001192*((PCT(I)/100.)**(-0.9663)) 
     &        + 0.0010110*SITEAR(ISPC) - 0.007711*RELHT) 
        ELSEIF(ISPC .EQ. 13)THEN
          DG5=(D*BARK)*((-0.3428*(-0.1741-EXP(-0.1328*D))) 
     &        + 0.1145*(FLOAT(ICR(I))/100.) - 0.0001682*BA 
     &        - 0.00003978*PBA - 0.159400*((PCT(I)/100.)**(-0.1299)) 
     &        + 0.0006204*SITEAR(ISPC) + 0.02474*RELHT) 
        ENDIF
        IF(DG5 .LT. 0.01)DG5=0.01
        DDS = ALOG((DG5*(2.0*D*BARK+DG5)))
        IF(DEBUG)WRITE(JOSTND,220)ISPC,I,D,HT(I),BARK,ICR(I),BA,PBA,
     &  PCT(I),SITEAR(ISPC),RELHT,DG5,DDS
  220   FORMAT(' ISPC,I,D,H,BARK,ICR,BA,PBA,PCT,SI,',
     &  'RELHT,DG5,DDS= ',/,2I5,2F8.2,F8.5,I5,6F10.4,F10.6)
        GO TO 225  
      ENDIF
C----------
C  CALCULATION OF DDS FOR SOUTHERN VARIENT
C  THE FIRST GROUP OF TERMS REFLECT INDIVIDUALTREE AND STAND CONDITIONS
C----------
      DDS= CONSPP + INTERC(ISPC)
     &   + LDBH(ISPC) * ALOG(D)
     &   + DBH2(ISPC) * D * D
     &   + LCRWN(ISPC) * ALOG(FLOAT(ICR(I)))
     &   + HREL(ISPC) * RELHT
     &   + PLTB(ISPC) * BA
     &   + PNTBL(ISPC) * PBAL
C
C
      IF(DEBUG) THEN
        D1= CONSPP + INTERC(ISPC)
        D2= LDBH(ISPC) * ALOG(D)
        D3= DBH2(ISPC) * D * D
        D4= LCRWN(ISPC) * ALOG(FLOAT(ICR(I)))
        D5= HREL(ISPC) * RELHT
        D6= ISIO(ISPC) * SITEAR(ISPC)
        D7= PLTB(ISPC) * BA
        D8= PNTBL(ISPC) * PBAL
        D9= TANS(ISPC) * SLOPE
        D10= FCOS(ISPC) * SLOPE * COS(ASPECT)
        D11= FSIN(ISPC) * SLOPE * SIN(ASPECT)
C
        WRITE(JOSTND,222)D1,D2,D3,D4,D5,D6,D7,D8,D9,D10,D11
  222   FORMAT(' IN DGF ,D1,D2,D3,D4,D5,D6,D7,D8,D9,D10,D11=',
     &  /,15F7.3)
      ENDIF
C----------
C  THE THIRD GROUP OF TERMS
C  ARE BASED ON MODEL CALCULATED FOREST TYPE
C----------
      DDS= DDS
     &   + FTLOHD(ISPC) * KLOHD
     &   + FTNOHD(ISPC) * KNOHD
     &   + FTOKPN(ISPC) * KOKPN
     &   + FTSFHP(ISPC) * KSFHP
     &   + FTUPHD(ISPC) * KUPHD
     &   + FTUPOK(ISPC) * KUPOK
     &   + FTYLPN(ISPC) * KYLPN
     &   + PLANT(ISPC)  * KPLANT
C
      IF(DEBUG) THEN
        D1=  FTLOHD(ISPC) * KLOHD
        D2=  FTNOHD(ISPC) * KNOHD
        D3=  FTOKPN(ISPC) * KOKPN
        D4=  FTSFHP(ISPC) * KSFHP
        D5=  FTUPHD(ISPC) * KUPHD
        D6=  FTUPOK(ISPC) * KUPOK
        D7=  FTYLPN(ISPC) * KYLPN
        D8=  PLANT(ISPC)  * KPLANT
C
        WRITE(JOSTND,224)D1,D2,D3,D4,D5,D6,D7, D8
  224   FORMAT(' FOREST TYPE D1,D2,D3,D4,D5,D6,D7, D8 =',
     &  /,15F7.3)
      ENDIF
  225 CONTINUE
C
      IF(DDS.LT.-9.21) DDS=-9.21
C
      WK2(I)=DDS
C----------
C  END OF TREE LOOP.  PRINT DEBUG INFO IF DESIRED.
C----------
      IF(DEBUG)THEN
        WRITE(JOSTND,9001) I,ISPC,DDS
 9001   FORMAT(' IN DGF, I=',I4,',  ISPC=',I3,',  DDS=',F7.4)
        WRITE(JOSTND,*)' IN DGF, DG= ',(DG(IK), IK= 1,50)
      ENDIF
   10 CONTINUE
C----------
C  END OF SPECIES LOOP.
C----------
   20 CONTINUE
      IF(DEBUG) WRITE(JOSTND,100)ICYC
  100 FORMAT(' LEAVING SUBROUTINE DGF  CYCLE =',I5)
      RETURN
      ENTRY DGCONS
C----------
C  ENTRY POINT FOR LOADING COEFFICIENTS OF THE DIAMETER INCREMENT
C  MODEL THAT ARE SITE SPECIFIC AND NEED ONLY BE RESOLVED ONCE.
C  OBSERV CONTAINS THE NUMBER OF
C  OBSERVATIONS BY SPECIES FOR THE UNDERLYING
C  MODEL (THIS DATA IS ACTUALLY USED BY **DGDRIV** FOR CALIBRATION).
C----------
C  CHECK FOR DEBUG.
C----------
      CALL DBCHK (DEBUG,'DGF',3,ICYC)
C
      DATA INTERC/
     & -2.267851,  -1.864431,  -2.267851,  -3.791466,  -0.008942,
     & -1.641698,  -2.431165,  -1.331052,  -2.600803,  -3.639059,
     & -2.353114,  -3.497764,   0.222214,  -2.600803,  -1.735969,
     & -4.224977,  -2.267851,  -1.685778,  -0.871047,  -2.260482,
     & -2.260482,  -2.313444,  -1.876225,  -1.092055,  -1.092055,
     & -1.281144,  -2.728289,  -1.068980,  -0.833167,  -1.062539,
     & -2.540719,  -2.524455,  -1.251887,  -2.954457,  -1.315283,
     & -0.897707,  -0.897707,  -0.314922,  -2.514589,  -2.352258,
     & -1.981934,  -2.354190,  -2.354190,  -1.324147,  -2.513351,
     & -2.516823,  -1.239592,  -1.477929,  -2.516823,  -1.477929,
     & -1.746231,  -1.746231,  -2.721782,  -1.508549,  -2.555720,
     & -1.431973,  -3.180040,  -2.096616,  -1.012995,  -1.068980,
     & -1.068980,  -2.610023,  -1.608339,  -2.284302,  -0.783581,
     & -0.295485,  -2.698130,  -1.561284,  -0.947174,  -1.948938,
     & -1.321662,  -2.223515,  -0.845477,  -2.900655,  -2.732646,
     & -0.328678,  -1.430321,  -2.345845,  -3.640660,  -1.307911,
     & -1.109398,  -1.745126,  -1.848106,  -2.356235,  -0.790138,
     & -0.510736,  -0.229212,  -1.864431,  -1.431973,  -1.645961/
C
      DATA LDBH/
     & 1.442529,   1.403065,   1.442529,   1.796179,   1.238170,
     & 1.461093,   1.691731,   1.098112,   1.525435,   1.397394,
     & 1.425614,   1.339503,   1.163040,   1.525435,   1.505649,
     & 1.831739,   1.442529,   1.454506,   1.217898,   1.449834,
     & 1.449834,   1.350084,   1.197048,   1.024946,   1.024946,
     & 1.335625,   1.548449,   1.164191,   1.190567,   1.174050,
