      SUBROUTINE FINDAG(I,ISPC,D1,D2,H,SITAGE,SITHT,AGMAX1,HTMAX1,
     &                  HTMAX2,DEBUG)
      IMPLICIT NONE
C----------
C AK $Id$
C----------
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
CC                                                                   CC
CC THERE ARE N O SITE CURVES FOR NEW ALASKA VARIANT SO THIS ROUTINE  CC
CC DOES NOTHING. ONLY RETAINED FOR BASE CODE COMPATIBILITY.          CC
CC                                                                   CC
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C
C  IN OTHER VARIANTS WITH SITE CURVES AVAILABLE, ITS PURPOSE IS TO 
C  DETERMINE TREE AGE BASED ON HEIGHT OR HEIGHT BASED ON TREE AGE.
C
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'

      INCLUDE 'CONTRL.F77'
C
C
COMMONS
C
C----------
C  VARIABLE DECLARATIONS:
C----------

      LOGICAL DEBUG

      INTEGER I,ISPC

      REAL AGMAX1,D1,D2,H,HTMAX1,HTMAX2
      REAL SITAGE,SITHT


        IF(DEBUG) WRITE(JOSTND,1) I,ISPC,
     &                  D1,D2,H,SITAGE,SITHT,AGMAX1,HTMAX1,HTMAX2
    1   FORMAT(' IN FINDAG AK STUB --I,ISPC=',2I5,
     &  ' D1,D2,H,SITAGE,SITHT,AGMAX1,HTMAX1,HTMAX2 ',8F10.2)

      RETURN
      END
