      DO 39 I=ISTART,IEND
      N=IPPREP(I)
      IF(N.LT.1.OR.N.GT.4) N=1
      NNPREP(N)=NNPREP(N)+1
      IF(DEBUG)WRITE(JOSTND,*) ' INSIDE LOOP FOR ASSIGNING SITE PREP',
