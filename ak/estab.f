     & IPPREP(N),' IPPREP(J)=',IPPREP(J)
  247 CONTINUE
  251 CONTINUE
      GO TO 49
  249 CONTINUE
      IF(DEBUG)WRITE(JOSTND,*)' WRITING IALN VARIABLES',' IALN(2)=',
     & IALN(2),' IALN(3)=',IALN(3)
C  BRANCH TO 246 IF USER HAS NOT SUPPLIED SITE PREPARATION KEYWORDS.
      IF(IALN(2).EQ.0.AND.IALN(3).EQ.0) GO TO 246
      SUM0=PMECH+PBURN
      IF(DEBUG)WRITE(JOSTND,*)' SUM(0)=',SUM0,' PMECH=',PMECH,
     & ' PBURN=',PBURN
      IF(SUM0.LE.1.0) GO TO 248
      PMECH=PMECH/SUM0
      PBURN=PBURN/SUM0
      IF(DEBUG)WRITE(JOSTND,*)'PMECH AND PBURN AFTER DIVISION OF SUM0.',
     &' SUM(0)=',SUM0,' PMECH=',PMECH,' PBURN=',PBURN
  248 CONTINUE
      PNONE=1.0-PMECH-PBURN
      IF(DEBUG)WRITE(JOSTND,*)' PNONE=', PNONE,' PMECH=',PMECH,
     &' PBURN=',PBURN
      GO TO 252
  246 CONTINUE
C     USER HAS NOT SUPPLIED ANY SITE PREPARATION KEYWORDS.
C     CALL ESPREP TO GET SITE PREPARATION VALUES.
      CALL ESPREP (PNONE,PMECH,PBURN)
  252 CONTINUE
C     SECTION TO CHOOSE SITE PREPS.  SAMPLE WITHOUT REPLACEMENT.
