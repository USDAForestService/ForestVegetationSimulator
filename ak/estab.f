      SUBROUTINE ESTAB (KDT)
      IMPLICIT NONE
C----------
C AK $Id$
C----------
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'ESPARM.F77'
C
C
      INCLUDE 'ARRAYS.F77'
C
C
      INCLUDE 'CALDEN.F77'
C
C
      INCLUDE 'CONTRL.F77'
C
C
      INCLUDE 'ESCOM2.F77'
C
C
      INCLUDE 'ESCOMN.F77'
C
C
      INCLUDE 'ESHAP.F77'
C
C
      INCLUDE 'ESHAP2.F77'
C
C
      INCLUDE 'ESTREE.F77'
C
C
      INCLUDE 'PDEN.F77'
C
C
      INCLUDE 'PLOT.F77'
C
C
      INCLUDE 'STDSTK.F77'
C
C
      INCLUDE 'VARCOM.F77'
C
COMMONS
C
C----------
C     REGENERATION ESTABLISHMENT MODEL   --   VERSION 2.0
C     USDA FOREST SERVICE, INTERMOUNTAIN RESEARCH STATION
C     1221 S. MAIN, MOSCOW, IDAHO  83843   (208) 882-3557
C
C     THIS PROGRAM MODELS REGENERATION ESTABLISHMENT IN THE ALASKA 
C     VARIANT. EXISTING MODELS REPLACED IN 2020 REFIT
C
C----------
C SPECIES LIST FOR ALASKA VARIANT.
C
C Number Code  Common Name         FIA  PLANTS Scientific Name           MIN.HT
C ------ ----  -----------         ---  ------ ---------------           ------
C   1     SF   Pacific silver fir  011  ABAM   Abies amabilis            1.0 FT
C   2     AF   subalpine fir       019  ABLA   Abies lasiocarpa          0.5 FT
C   3     YC   Alaska cedar        042  CANO9  Callitropsis nootkatensis 0.5 FT
C   4     TA   tamarack            071  LALA   Larix laricina            0.5 FT
C   5     WS   white spruce        094  PIGL   Picea glauca              0.5 FT
C   6     LS   Lutz’s spruce            PILU   Picea lutzii              0.5 FT
C   7     BE   black spruce        095  PIMA   Picea mariana             0.5 FT
C   8     SS   Sitka spruce        098  PISI   Picea sitchensis          0.5 FT
C   9     LP   lodgepole pine      108  PICO   Pinus contorta            1.0 FT
C  10     RC   western redcedar    242  THPL   Thuja plicata             0.5 FT
C  11     WH   western hemlock     263  TSHE   Tsuga heterophylla        0.5 FT
C  12     MH   mountain hemlock    264  TSME   Tsuga mertensiana         0.5 FT
C  13     OS   other softwoods     298  2TE                              0.5 FT
C  14     AD   alder species       350  ALNUS  Alnus species             1.0 FT 
C  15     RA   red alder           351  ALRU2  Alnus rubra               1.0 FT
C  16     PB   paper birch         375  BEPA   Betula papyrifera         1.0 FT
C  17     AB   Alaska birch        376  BENE4  Betula neoalaskana        1.0 FT
C  18     BA   balsam poplar       741  POBA2  Populus balsamifera       1.0 FT
C  19     AS   quaking aspen       746  POTR5  Populus tremuloides       1.0 FT
C  20     CW   black cottonwood    747  POBAT  Populus trichocarpa       1.0 FT
C  21     WI   willow species      920  SALIX  Salix species             1.0 FT
C  22     SU   Scouler’s willow    928  SASC   Salix scouleriana         1.0 FT
C  23     OH   other hardwoods     998  2TD                              1.0 FT
C----------
C  VARIABLE DECLARATIONS:
C----------
      EXTERNAL ESRANN

      LOGICAL DEBUG

      CHARACTER CBLANK*2

      CHARACTER*2 CLAST(MAXSP)
      CHARACTER*4 CPREP(4),CTOPO(5)
      CHARACTER*47 HABTYP(16)
      CHARACTER*7  SERIES(16)
      CHARACTER*3  FINAL

      INTEGER I,IACTK,IBRKUP,IDO,IDUP,IEND,IESFT,IEVTYR,II,INGRO,IPNSPE
      INTEGER IPOLD,IPYEAR,IREP,ISER,ISHADE,ISTART,ITIME,ITODO,ITP,M
      INTEGER ITPP,ITRNIN,ITYPEP,IZERO,J,JJ,K,KDT,L,MATCH,MYDO,MXRR
      INTEGER N,NBEST,NCOUNT,NDRAW,NEWTPP,NN,NOFSPE,NP,NSPNZ,NTIMES
      INTEGER NTODO,NUM2PK,NUMSPE,IFT

      INTEGER*4 IDCMP1,ITEMP,ITMP,ITOOMP

      INTEGER IASEP(99),IBEST(MAXSP),ICHOI(2,MAXSP),ICODE(99)
      INTEGER ILSP(MAXSP),IPLANT(MAXSP),MAXING(14),MAXSPP(14)
      INTEGER MAXTPP(14),MYACTS(4),MYHABG(16),NNPREP(4)
      INTEGER NOTE(99),JNULL(MAXSP)

      REAL BAAOLD,BACHLO,BRKUP,CRDUM,CW,DELAY,DILATE,DRAW,DUP,DUPNPT
      REAL EMSQR,ESAVE,EXPAND,FLOKDT,FLONPT,FTEMP,FTEMP2,GENTIM,HHT
      REAL PASMAX,PN,PNONE,PTREE,RADIAN,SHADE,SHORTY
      REAL SUM0,SUM1,SUM2,TBAAA,TCROP1,TCROP2,TMTIME,TPP,TRAGE,TREEHT
      REAL TTOTTP,XCSMAX,XH,XXH,ZHARV,WTAVEHT
      REAL PTA,TPAOLD

      REAL AGADSB(99),AGEPL(99),AGEXC(99),AVEHT(MAXSP),BESPCT(MAXSP)
      REAL BESTPA(MAXSP),ESPROB(99),EXCESS(MAXSP),FIRST(2,MAXSP)
      REAL HEIGHT(99),HTIMLT(99),PASPCT(MAXSP),PASTPA(MAXSP),PRMS(6)
      REAL PSPE(6),STOMLT(MAXSP),SUMESP(MAXSP),SUMHTS(MAXSP)
      REAL SUMUP(MAXSP),TALL(MAXSP),TOTPCT(MAXSP),TOTTPA(MAXSP)
C----------
C  DATA STATEMENTS:
C----------
      DATA CBLANK/'  '/,
     & CPREP/'NONE','MECH','BURN','ROAD'/,
     & CTOPO/'BOTM','LOWR',' MID','UPPR','RIDG'/,
     & IDCMP1/10000000/,
     & MYACTS/430,431,491,493/,
     & NOFSPE/MAXSP/
C----------
C MAXTPP: CONTAINS MAX TPP VALUES USED IN TALLY SEQUENCES. VALUES ARE
C BASED ON THE 75TH PERCENTILE TPP VALUES FOUND IN THE DATA FOR
C EACH FOREST TYPE.
C
C MAXSPP: CONTAINS THE 90TH PERCENTILE OF NUMBER OF SPECIES THAT CAN
C OCCUR ON A STOCKED PLOT BY FOREST TYPE.
C
C MAXING: CONTAINS MAX TPP VALUES USED IN INGROWTH PREDICTIONS BY FOREST
C TYPE. VALUES ARE ALL SET TO 3.FOR MOST FOREST TYPES, A VALUE OF 3
C WAS CLOSE TO THE 25TH PERCENTILE TPP VALUE. THE 25TH PERCENTILE TPP
C VALUE WAS GREATER THAN 3 FOR BE, YC, LP, AND RC, FOREST TYPES BUT
C THESE VALUES YIELDED INGROWTH VALUES THAT SEEMED TO HIGH FOR 20
C YEAR INGROWTH INTERVALS.
C-----------
      DATA
     &  MAXTPP/17,30,10,22,21,21,21,10,10,15,15,17,5,0/,
     &  MYHABG/4*1,4*2,3,4,6*5/,
     &  MAXSPP/2,2,3,4,4,3,4,2,2,2,3,2,2,0/,
     &  MAXING/3,3,3,3,3,3,3,3,3,3,3,3,3,0/
      DATA HABTYP/
     &  'PSME/ VAGL, LIBO, VACA',
     &  'PSME/ CARU, CAGE, GRASS',
     &  'PSME/ PHMA, ACGL',
     &  'PSME/ SYAL, SPBE, SYOR, ARUV, ARCO',
     &  'ABGR/ LIBO, CLUN-XETE',
     &  'ABGR/ XETE, VAGL, COOC, VACA',
     &  'ABGR/ CLUN (EXCEPT CLUN-XETE)',
     &  'ABGR/ SPBE, ACGL, PHMA, ASCA, SETR',
     &  'THPL/ ALL',
     &  'TSHE/ ALL',
     &  'ABLA/ VAGL, VASC, VACA',
     &  'ABLA/ XETE, LIBO',
     &  'ABLA/ CLUN, GATR',
     &  'ABLA/ CAGE, CARU, ACGL, SPBE',
     &  'ABLA/ MEFE, ALSI, TSME/ CLUN, XETE, MEFE, STAM',
     &  'ABLA/ CACA, STAM, LUHI'/
      DATA SERIES/
     &  'DF     ',
     &  'DF     ',
     &  'DF     ',
     &  'DF     ',
     &  'GF     ',
     &  'GF     ',
     &  'GF     ',
     &  'GF     ',
     &  'C      ',
     &  'WH     ',
     &  'AF & MH',
     &  'AF & MH',
     &  'AF & MH',
     &  'AF & MH',
     &  'AF & MH',
     &  'AF & MH'/
      DATA FINAL/"ALL"/

      CALL DBCHK (DEBUG,'ESTAB',5,ICYC)
      IF(DEBUG) WRITE(JOSTND,7000) KDT,IDSDAT,NTALLY
 7000 FORMAT(/'IN ESTAB; BEGIN DEBUG.  KDT=',I5,' IDSDAT=',I5,
     >        ' NTALLY=',I2)
C----------
C STEP 1: INITIALIZE ARRAYS AND VARIABLES, AND REPLICATE PLOTS
C----------
      IF(DEBUG) WRITE(JOSTND,9991) 
 9991 FORMAT(/,'STEP 1: INITIALIZE ARRAYS AND VARIABLES,'
     >' AND REPLICATE PLOTS.')     
      ITRNIN=ITRN+1
      IF(DEBUG) WRITE(JOSTND,*)'ITRNIN:',ITRNIN,'ITRN:', ITRN
      INGRO=0
      IF(DEBUG) WRITE(JOSTND,*)'INGRO:',INGRO
C     ZHARV IS DATE OF DISTURBANCE, SET TO YEAR OF HARVEST      
      ZHARV=REAL(IDSDAT)
      IF(DEBUG) WRITE(JOSTND,*)'ZHARV IS SET TO DATE OF DISTURBANCE',
     > ' ZHARV:',ZHARV,'IDSDAT:', IDSDAT
C     FLOKDT IS YEAR OF TALLY (FALL OF LAST YEAR OF CYCLE)
      FLOKDT=REAL(KDT)
      IF(DEBUG) WRITE(JOSTND,*)'FLOKDT IS YEAR OF TALLY. FLOKDT:',
     >FLOKDT
C     TIME (GLOBAL) IS YEARS SINCE DISTURBANCE
      TIME=FLOKDT - ZHARV +1.0
      IF(DEBUG) WRITE(JOSTND,*)'TIME IS YEARS SINCE DISTURBANCE AND ',
     >'SET TO FLOKDT-ZHARV+1. '
      IF(DEBUG) WRITE(JOSTND,*)' TIME:',TIME,'FLOKDT:',FLOKDT,'ZHARV:',
     >ZHARV
C     FLONPT (LOCAL) IS THE NUMBER OF INVENTORY POINTS IN BASE MODEL      
      FLONPT=REAL(NPTIDS)
      IF(DEBUG) WRITE(JOSTND,*)'FLONPT IS # OF INV. POINTS IN BASE. ',
     >'FLONPT:',FLONPT
      SUMPRB=0.0
      ELEVSQ=ELEV*ELEV
      IF(DEBUG) WRITE(JOSTND,*)'ELEVATION SET TO STAND ELEVATION. ',
     >'ELEV:', ELEV,'ELEVSQ:',ELEVSQ
C     ITMP IS SET TO CURRENT CYCLE*10000, I DON'T KNOW WHY YET
      ITMP=ICYC
      ITMP=ITMP*10000
      IF(DEBUG) WRITE(JOSTND,*)'ITMP IS SET TO CURRENT CYCLE*10000.',
     >' ITMP:',ITMP, 'ICYC:', ICYC
C     TURN ON FULL ESTABLISHMENT MODEL (0), PLANT AND NATURAL ONLY (1)      
      MODE=0
      IF(DEBUG) WRITE(JOSTND,*)'IF MODE=0, TURN ON FULL ESTAB. IF ',
     >'MODE=1, PLANT AND NATURAL ONLY. MODE:', MODE
C     IF TALLY DATE IS 20 YEARS PAST INVENTORY, SET FVS TO ADD
C     REGENERATION FROM ADVANCED COMPONENT. DO NOT LOAD SITE PREP FROM
C     INPUT DATA       
      IF(KDT+1-IY(1).GT.20) THEN
        INADV=1
        LOAD=0
      ENDIF
      IF(DEBUG) WRITE(JOSTND,*)'IF (KDT+1-IY(1).GT.20) THEN INADV=1 ',
     >'AND LOAD=0.'
      IF(DEBUG) WRITE(JOSTND,*)' KDT:',KDT,'IY(1):', IY(1),' INADV:',
     >INADV,'LOAD:',LOAD
C     PASMAX IS SET TO THE NUMBER OF EXCESS TREES PER SPECIES PASSED
C     TO MAIN MODEL      
      PASMAX=CONFID
      IF(DEBUG) WRITE(JOSTND,*)'PASMAX SET TO # OF EXCESS TREES TO ',
     >'PASS TO MAIN MODEL.'
      IF(DEBUG) WRITE(JOSTND,*)' PASMAX:', PASMAX, 'CONFID:',CONFID
      DO 40 I= 1,99
      NOTE(I)= 0
      AGADSB(I)=0.0
      AGEPL(I)=0.0
      AGEXC(I)=0.0
   40 CONTINUE
      IF(DEBUG) WRITE(JOSTND,*)'NOTE, AGADSB, AGEPL, AGEXC ALL SET TO',
     >' 0, 99 TIMES.'     
      IF(DEBUG) WRITE(JOSTND,*)' NOTE:', NOTE  
      IF(DEBUG) WRITE(JOSTND,*)' AGADSB:', AGADSB 
      IF(DEBUG) WRITE(JOSTND,*)' AGEPL:', AGEPL 
      IF(DEBUG) WRITE(JOSTND,*)' AGEXC:', AGEXC             
      IF(DEBUG) WRITE(JOSTND,*)'FIRST IS A 2x23 array. ALL VALUES SET',
     >' TO 0.1.' 
      DO 41 I=1,2
      DO 41 N=1,NOFSPE
      FIRST(I,N)=0.1
      IF(DEBUG) WRITE(JOSTND,*)' FIRST(I,N). I:',I,'N:',N,'FIRST:',
     >FIRST(I,N) 
   41 CONTINUE
   
C     SUMMATION OF TREES TO PASS TO MAIN MODEL
      TCROP1=0.0
      TCROP2=0.0
      TTOTTP=0.0
      IF(DEBUG) WRITE(JOSTND,*)'SUMMATION VARIABLES TO PASS TO MAIN',
     >' MODEL. TCROP1:', TCROP1,'TCROP2:', TCROP2,'TTOTTP:', TTOTTP             
C     SOME SPECIES LEVEL ATTRIBUTES 
      IF(DEBUG) WRITE(JOSTND,*)'SPECIES ATTRIBUTES INITIALIZED.'
      DO 181 I=1,NOFSPE
      TOTTPA(I)=0.0
      AVEHT(I)=0.0
      SUMPX(I)=0.0
      SUMPI(I)=0.0
      BESTPA(I)=0.0
      PASTPA(I)=0.0
      IPLANT(I)=0
      IF(DEBUG) WRITE(JOSTND,*)' I:',I, 'TOTTPA:', TOTTPA(I),'AVEHT:',
     >AVEHT(I),'SUMPX:', SUMPX(I),'SUMPI:', SUMPI(I), 'BESTPA:', 
     >BESTPA(I),'PASTPA:', PASTPA(I),'IPLANT:', IPLANT(I)
  181 CONTINUE

C   CALCULATE FOREST TYPE FOR TALLY SEQUENCE
C
C   IFORTP IS CURRENT STAND FOREST TYPE, FROM PLOT.F77 AND IF IT HAS
C   BECOME UNDEFINED, THE INVENTORY FOREST TYPE (IIFORTP) WILL BE USED.

      SELECT CASE (IFORTP)
        CASE (122, 125, 270, 271, 281, 301, 304, 305, 
     &        703, 901, 902, 904, 911) 
          IESFT = IFORTP
        CASE DEFAULT 
          IESFT = IIFORTP
      END SELECT
      IF(DEBUG) WRITE(JOSTND,*)'SET FOREST TYPE. IF CURRENTLY ',
     >'UNDEFINED, SET TO INVENTORY FOREST TYPE.'
      IF(DEBUG) WRITE(JOSTND,*)' IESFT:', IESFT,
     >'CURRENT FT IFORTP:',IFORTP,'INVENTORY FT IIFORTP:',IIFORTP
C   THIS FOREST TYPE WILL BE USED THROUGHOUT THE DURATION OF
C   THE TALLY SEQUENCE
C   SET IFT TO FOREST TYPE GROUPS 1-13
C
C   IFT USED TO SET PLOT SIZE AND DETERMINING PROBABILITIES
      SELECT CASE (IESFT)
        CASE (122)
          IFT = 1
        CASE (125)
          IFT = 2
        CASE (270)
          IFT = 3
        CASE (271)
          IFT = 4
        CASE (281)
          IFT = 5
        CASE (301)
          IFT = 6
        CASE (304)
          IFT = 7
        CASE (305)
          IFT = 8
        CASE (703)
          IFT = 9
        CASE (901)
          IFT = 10
        CASE (902)
          IFT = 11
        CASE (904)
          IFT = 12
        CASE (911)
          IFT = 13
        CASE DEFAULT 
          IFT = 14
      END SELECT
C DISPLAY IFT
      IF(DEBUG) WRITE(JOSTND,*) 'IFT DETERMINED FROM FOREST TYPE: ', IFT
C STORE IFT0 IF GOING INTO A TALLY SEQUENCE
      IF(NTALLY .EQ. 1) THEN
        IFT0 = IFT
        IF(DEBUG) WRITE(JOSTND, *) 'IF FIRST TALLY, STORE FOREST ',
     >  'TYPE. IFT0: ', IFT0, 'IFT', IFT
      ENDIF
C USE IFT0 IF IN A TALLY SEQUENCE
      IF(NTALLY .LT. 99) THEN
        IFT = IFT0
        IF(DEBUG) WRITE(JOSTND, *) 'IF IN TALLY SEQUENCE, SET FOREST ', 
     >  ' TYPE TO FIRST TALLY FOREST TYPE.' 
        IF(DEBUG) WRITE(JOSTND, *) ' IFT: ', IFT,'IFT0:',IFT0
      ENDIF
C     PLOT REPLICATION, MINREP SET IN ESINIT OR BY USER 
C     DUP IS THE NUMBER OF DUPLICATE REGENERATION PLOTS PER BASE
C     MODEL PLOT
      IF(DEBUG) WRITE(JOSTND,*)'CREATE REGENERATION PLOTS BY ',
     >'DETERMINING NUMBER OF REPLICATES OF BASE MODEL PLOTS.' 
      IF(DEBUG) WRITE(JOSTND,*)'TARGET NUMBER OF REPLICATES. MINREP:', 
     >MINREP     
      DUP=0.0
      DO 138 I=1,MINREP
      DUP=DUP+1.0
      N=NPTIDS*I
      IF(NPTIDS*(I+1).GT.MAXPLT) GO TO 139
      IF(N.GE.MINREP) GO TO 139
  138 CONTINUE
  139 CONTINUE
      IDUP = INT(DUP+0.5)
C     DUPNPT IS THE TOTAL NUMBER OF DUPLICATE PLOTS      
      DUPNPT=FLONPT*DUP
      IF(DEBUG) WRITE(JOSTND,*)'TOTAL NUMBER OF REGENERTION PLOTS:',
     >DUPNPT,'FROM',FLONPT,'BASE PLOTS AND',DUP,'DUPLICATES.'
C     EXPAND IS THE INVERSE OF INVENTORY PLOT SIZE USED IN FITTING
C     ESTABLISHMENT MODEL RELATIONSHIPS. SET BY FOREST TYPE. 
C     BOREAL FOREST TYPES SET TO 1/250TH (UAF DATASET)
C     COASTAL SET TO 1/300TH (FIA DATASET)
      SELECT CASE (IFT)
        CASE (1,2,10,11,12)
          EXPAND = 250.
        CASE DEFAULT 
          EXPAND = 300. 
      END SELECT 
      IF(DEBUG) WRITE(JOSTND,*)'EXPAND IS USED TO DETERMINE TPA OF',
     >' RECORDS BASED ON PLOT SIZE OF FITTING DATASET.'
      IF(DEBUG) WRITE(JOSTND,*)'COASTAL FOREST TYPES SET TO 300 AND ', 
     >'BOREAL SET TO 250. EXPAND:', EXPAND 
C----------
C STEP 2: INITIALIZE REGENERATION VECTORS AND CALIBRATION TASKS
C
C VARIABLE DEFINITIONS
C XTES    -- RATIO OF REMOVALS AS CALCULATED IN ESNUTR.
C THRES1  -- LOWER THRESHOLD OF REMOVALS THAT TRIGGERS ONE TALLY.
C ZHARV   -- DATE OF HARVEST
C ZMECH   -- DATE OF MECHANICAL SITE PREP
C ZBURN   -- DATE OF BURN SITE PREP
C ITEMP   -- VARIABLE USED TO DEFINE VALUE OF SHORTY IN INGROWTH
C            INITIALIZATION
C ITRN    -- NUMBER OF TREE RECORDS
C FTEMP   -- DIAMETER OF TREE RECORD
C NN      -- SPECIES OF TREE RECORD
C FTEMP2  -- TREES PER ACRE OF TREE RECORD
C PASTTPA -- SUM OF TREES PER ACRE BY SPECIES
C TCROP2  -- SUM OF TREES PER ACRE
C SUM0    -- SUM OF TREES PER ACRE
C N       -- PLOT NUMBER
C PLPROB  -- STAND LEVEL TPA ESTIMATE OF PLOT REPLICATES, SUM TO GET
C            STAND TPA
C IALN    -- ARRAY CONTAINING VALUES INDICATING WHETHER SITE PREP WAS
C            ENTERED OR NOT. 0 IF NOT SITE PREP IS ENTERED AND 1 IF
C            SITE PREP WAS SPECIFIED.
C NPTIDS  -- NUMBER OF INVENTORY PLOTS.
C----------
C     IF NOT THE FIRST TALLY, CANCEL MECH & BURNPREP'S IN THIS CYCLE.
      IF(DEBUG) WRITE(JOSTND,9992) 
 9992 FORMAT(/,'STEP 2: INITIALIZE REGENERATION ',
     >'VECTORS AND CALIBRATION TASKS.') 
      IF(NTALLY.GT.1) THEN
        CALL OPFIND (2,MYACTS(3),NTODO)
        IF(NTODO.GT.0) THEN
          DO 6 ITODO=1,NTODO
          CALL OPDEL1 (ITODO)
    6     CONTINUE
        ENDIF
      ENDIF
      IF(DEBUG)WRITE(JOSTND,*)' NTALLY=',NTALLY
C     DO NOT CREATE TREE RECORDS THAT WOULD ESSENTIALLY BE REDUNDANT
C     WITH THOSE IN THE INVENTORY WHEN NTALLY IS 1 AND 20 YEARS HAS
C     ELAPSED SINCE THE INVENTORY (I.E. NO CALIBRATION).
      MATCH=0
      IF(NTALLY.EQ.1.AND.KDT+1-IY(1).GT.20) MATCH=1
      IF(DEBUG)WRITE(JOSTND,*) ' NTALLY=',NTALLY, ' KDT=', KDT, ' IY',
     &  IY(1),' MATCH=',MATCH
C     WORK WITH INGROWTH (99) OPTION - INITIALIZE PARAMETERS
      IF(NTALLY.EQ.99) THEN
C       PNONE IS THE PROPORTION OF PLOTS UNTREATED, SET TO 100%  
        PNONE=1.0
        PMECH=0.0
        PBURN=0.0
C       METH IS SITE PREP METHOD: 1=NONE,2=MECH,3=BURN,4=ROADS
        METH=0
        LOAD=0
        IALN(2)=1
        INGRO=1
        PASMAX=15.0
C       ITEMP IS A TIME ESTIMATE, SET TO 1 HERE -INGROWTH SECTION
        ITEMP = INT(((XTES/THRES1) *FINT) +0.5)
        IF(ITEMP.LT.1) ITEMP=1
        SHORTY=REAL(ITEMP)
C       DATES OF SITE PREPS
        ZHARV=REAL(KDT+1) -SHORTY
        ZMECH=ZHARV
        ZBURN=ZHARV
        IF(DEBUG)WRITE(JOSTND,*) 'INGROWTH INITIALIZATION TIME',
     &  ' VARIABLES.',' ITEMP=',ITEMP,' XTES=', XTES,' THRES1=',THRES1,
     &  ' FINT=',FINT,' SHORTY=',SHORTY,' ZHARV=',ZHARV,' ZMECH=',ZMECH,
     &  ' ZBURN=',ZBURN
        NTALLY=1
        GO TO 44
      ENDIF
C     IF NOT THE FIRST TALLY, THEN SKIP SITE PREPS.
      IF(NTALLY.NE.1) GO TO 276
C     PROCESS SITE PREPARATION KEYWORDS      
      CALL ESETPR (METH,ZMECH,ZBURN,PNONE,PMECH,PBURN,IALN,
     &  IDSDAT,KDT,LOAD)
C     SET DATES OF MECH AND BURN SITE PREP TO OCCUR NOT BEFORE 
C     HARVEST DATE     
      IF(ZMECH.LT.ZHARV) ZMECH=ZHARV
      IF(ZBURN.LT.ZHARV) ZBURN=ZHARV
      IF(DEBUG) WRITE(JOSTND,7011) METH,IDSDAT,KDT,ZMECH,ZBURN,
     &  PNONE,PMECH,PBURN,(IALN(I),I=1,3)
 7011 FORMAT('AFTER ESETPR: METH=',I2,' IDSDAT=',I5,' KDT=',I5,
     &  ' ZMECH=',F7.1,' ZBURN=',F7.1,/,4X,'PNONE=',F7.4,' PMECH=',
     &  F7.4,' PBURN=',F7.4,3X,'IALN(I)=',3I5)
   44 CONTINUE
C     KDTOLD IS LAST CYCLE TALLY DATE
      KDTOLD = INT(ZHARV-0.5)
      IF(DEBUG)WRITE(JOSTND,*) ' KDTOLD=',KDTOLD
C     INITIALIZE SUMPRE(1:5) TO 0. SUMPRE IS SUMMARY COUNT OF SITE PREPS
C     1=NONE, 2=MECH, 3=BURN, 4&5=?
      DO 45 I=1,5
      SUMPRE(I)=0.0
   45 CONTINUE
C     INITIALIZE IHABT TO 0. IHABT IS SUMMARY COUNT OF HABITAT TYPES
      DO 46 I=1,16
      IHABT(I)=0
   46 CONTINUE
C     INITIALIZE VARIABLES ON ESTAB MODEL REGENERATION PLOTS
      DO 12 I=1,NPTIDS*IDUP
      ESB1(I)=0.0
      PNN(I)=0.0
      PLPROB(I)=0.0
      NSTORE(I)=0
   12 CONTINUE
      ESB=0.0
  276 CONTINUE
C     IF FIRST TALLY, SET ITEMP TO RANDOM NUMBER. ESDRAW IS REAL OF 
C     CALL TO ESDRAW SETS UP RANDOM NUMBER SEED
      IF(NTALLY.EQ.1) THEN
        CALL ESRANN (DRAW)
        ITEMP = INT(DRAW*100000.0 + 0.5)
        ESDRAW=REAL(ITEMP)
      ENDIF
      CALL ESRNSD (.TRUE.,ESDRAW)
      IF(DEBUG) WRITE(JOSTND,7014) ESDRAW
 7014 FORMAT('RANDOM NUMBER SEED=',F9.2)
C     LOAD REGENERATION VECTORS FROM CURRENT INVENTORY.
