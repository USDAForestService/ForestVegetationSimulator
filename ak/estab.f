 2451 CONTINUE
      IF(DEBUG)WRITE(JOSTND,*)'SUM1,SUM2= ',SUM1,SUM2
C---------------------------------------------------------------------
C     BEGIN PROCESSING OF PLOTS                             HERE WE GO
C     TRIPLE DO-LOOP TO DETERMINE REGENERATION RECORDS TO ADD TO EACH 
