  245 CONTINUE
      DO 2451 NN=1,NPTIDS
      NNID=IPTIDS(NN)
      TBAAA=BAAA(NNID)
      IF(TBAAA .LT. 1.)TBAAA=1.
      SUM2=SUM2+SUM1/TBAAA
      IF(DEBUG)WRITE(JOSTND,*)'IN SECOND SHADE ADJUSTMENT SUMS LOOP',
     & ' (2451).',' NPTID=',NN,' BAAA=',BAAA(NNID),' TBAAA=',TBAAA,
