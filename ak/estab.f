        MYDO=MYDO-1
      ENDIF
  244 CONTINUE
      IF(MYDO .EQ. 0) MODE=0
C     ACCUMULATE SUMS FOR CALCULATION OF SHADE ADJUSTMENTS
      SUM1=0.
      SUM2=0.
      DO 245 NN=1,NPTIDS
      NNID=IPTIDS(NN)
      TBAAA=BAAA(NNID)
      IF(TBAAA .LT. 1.)TBAAA=1.
      SUM1=SUM1+TBAAA
      IF(DEBUG)WRITE(JOSTND,*)' IN FIRST SHADE ADJUSTMENT SUMS LOOP.',
