C     IF LOAD=1, REP PLOT SITE PREPS; IF NEEDED COMPRESS ARRAY IPPREP.
      NN=0
C     LOOP ACROSS BASE MODEL PLOTS
      DO 243 I=1,NPTIDS
      NN=NN+1
      N=IPTIDS(I)
      IPPREP(NN)=IPPREP(N)
  243 CONTINUE
      IF(IDUP.LT.2) GO TO 49
C     NESTED LOOP TO SET SITE PREP BASED ON INVENTORY PLOTS     
      DO 251 I=1,IDUP
      DO 247 N=1,NPTIDS
      J=I*NPTIDS +N
      IPPREP(J)=IPPREP(N)
      IF(DEBUG)WRITE(JOSTND,*)' INSIDE SITE PREP SETTING LOOPS',
