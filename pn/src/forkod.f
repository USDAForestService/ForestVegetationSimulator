      SUBROUTINE FORKOD
      IMPLICIT NONE
C----------
C  **FORKOD--PN   DATE OF LAST REVISION:  04/10/08
C----------
C
C     TRANSLATES FOREST CODE INTO A SUBSCRIPT, IFOR, AND IF
C     KODFOR IS ZERO, THE ROUTINE RETURNS THE DEFAULT CODE.
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'PLOT.F77'
C
C
      INCLUDE 'CONTRL.F77'
C
C
COMMONS
C----------
C VALID FOREST CODES ARE:
C     609 = OLYMPIC
C     612 = SIUSLAW
C     800 = QUINAULT INDIAN RESERVATION
C     708 = BLM SALEM ADMIN UNIT
C     709 = BLM EUGENE ADMIN UNIT
C     712 = BLM COOS BAY ADMIN UNIT
C----------
      INTEGER JFOR(6),KFOR(6),NUMFOR,I
C
      DATA JFOR/609,612,800,708,709,712/, NUMFOR /6/
      DATA KFOR/1,1,1,1,1,1/
C
      IF (KODFOR .EQ. 0) GOTO 30
      DO 10 I=1,NUMFOR
      IF (KODFOR .EQ. JFOR(I)) GOTO 20
   10 CONTINUE
      CALL ERRGRO (.TRUE.,3)
      WRITE(JOSTND,55) JFOR(IFOR)
  55  FORMAT(T13,'FOREST CODE USED IN THIS PROJECTION IS ',I4)
      GOTO 30
   20 CONTINUE
      IFOR=I
      IGL=KFOR(I)
   30 CONTINUE
      KODFOR=JFOR(IFOR)
      RETURN
      END
