     & 90., 91., 82., 95., 79., 84., 82., 86., 70., 85., 93.,
     & 79., 75., 90., 80., 78., 78., 86., 91., 93., 82., 79.,
     & 75., 74., 79., 79., 79., 82., 74., 92., 96., 86., 65.,
     & 82., 95., 84., 92., 84., 78./                              
C----------
