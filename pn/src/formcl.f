          FC = BLM709(ISPC)
        ELSEIF (IFOR.EQ.6) THEN
          FC = BLM712(ISPC)
        ELSE
          FC = SIUSFC(ISPC,IFCDBH)
        ENDIF
      ELSE
        FC=FRMCLS(ISPC)
      ENDIF
