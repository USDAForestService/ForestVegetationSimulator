      SUBROUTINE FORKOD
      IMPLICIT NONE
C----------
C  **FORKOD--WC   DATE OF LAST REVISION:  05/19/08
C----------
C
C     TRANSLATES FOREST CODE INTO A SUBSCRIPT, IFOR, AND IF
C     KODFOR IS ZERO, THE ROUTINE RETURNS THE DEFAULT CODE.
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'PLOT.F77'
C
C
      INCLUDE 'CONTRL.F77'
C
C
COMMONS
C
C----------
C  NATIONAL FORESTS:
C  603 = GIFFORD PINCHOT
C  605 = MT BAKER - SNOQUALMIE
C  606 = MT HOOD 
C  610 = ROGUE RIVER
C  615 = UMPQUA
C  618 = WILLAMETTE
C  708 = BLM SALEM ADU
C  709 = BLM EUGENE ADU
C  710 = BLM ROSEBURG ADU
C  711 = BLM MEDFORD-LAKEVIEW ADU
C  613 = MT BAKER-SNOQUALMIE (MAP TO MT BAKER-SNOQUALMIE)
C----------
      INTEGER JFOR(11),KFOR(11),NUMFOR,I
      DATA JFOR/603,605,606,610,615,618,708,709,710,711,613/, NUMFOR/11/
      DATA KFOR/ 11*1 /
C
      IF (KODFOR .EQ. 0) GOTO 30
      DO 10 I=1,NUMFOR
      IF (KODFOR .EQ. JFOR(I)) GOTO 20
   10 CONTINUE
      CALL ERRGRO (.TRUE.,3)
      WRITE(JOSTND,55) JFOR(IFOR)
  55  FORMAT(T12,'FOREST CODE USED IN THIS PROJECTION IS ',I4)
      GOTO 30
   20 CONTINUE
      IF(I .EQ. 11)THEN
        WRITE(JOSTND,21)
   21   FORMAT(T12,'MT BAKER-SNOQUALMIE NF (613) BEING MAPPED TO ',
     &  'MT BAKER-SNOQUALMIE (605) FOR FURTHER PROCESSING.')
        I=2
      ENDIF
      IFOR=I
      IGL=KFOR(I)
   30 CONTINUE
      KODFOR=JFOR(IFOR)
C
      RETURN
      END
