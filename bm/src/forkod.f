      SUBROUTINE FORKOD
      IMPLICIT NONE
C----------
C  **FORKOD--BM   DATE OF LAST REVISION:  02/20/08
C----------
C
C     TRANSLATES FOREST CODE INTO A SUBSCRIPT, IFOR, AND IF
C     KODFOR IS ZERO, THE ROUTINE RETURNS THE DEFAULT CODE.
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'PLOT.F77'
C
C
      INCLUDE 'CONTRL.F77'
C
C
COMMONS
C
C----------
C  NATIONAL FORESTS:
C  604 = MALHEUR
C  607 = OCHOCO
C  614 = UMATILLA
C  616 = WALLOWA-WHITMAN
C  619 = WHITMAN (MAPPED TO WALLOWA-WHITMAN)
C----------
      INTEGER JFOR(7),KFOR(7),NUMFOR,I
      DATA JFOR/604,607,614,616,619, 0, 0 /, NUMFOR /5/
      DATA KFOR/1,1,1,1,1,1,1 /
C
      IF (KODFOR .EQ. 0) GOTO 30
      DO 10 I=1,NUMFOR
      IF (KODFOR .EQ. JFOR(I)) GOTO 20
   10 CONTINUE
      CALL ERRGRO (.TRUE.,3)
      WRITE(JOSTND,11) JFOR(IFOR)
   11 FORMAT(T12,'FOREST CODE USED IN THIS PROJECTION IS  ',I4)
      GOTO 30
   20 CONTINUE
      IF(I .EQ. 5)THEN
        WRITE(JOSTND,21)
   21   FORMAT(T12,'WHITMAN NF (619) BEING MAPPED TO WALLOWA-WHITMAN ',
     &  '(616) FOR FURTHER PROCESSING.')
        I=4
      ENDIF
      IFOR=I
      IGL=KFOR(I)
   30 CONTINUE
      KODFOR=JFOR(IFOR)
      RETURN
      END
