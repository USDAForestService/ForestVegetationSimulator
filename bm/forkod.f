      SUBROUTINE FORKOD
      IMPLICIT NONE
C----------
C BM $Id$
C----------
C
C     TRANSLATES FOREST CODE INTO A SUBSCRIPT, IFOR, AND IF
C     KODFOR IS ZERO, THE ROUTINE RETURNS THE DEFAULT CODE.
C----------
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'CONTRL.F77'
C
C
      INCLUDE 'PLOT.F77'
C
C
COMMONS
C
C----------
C  NATIONAL FORESTS:
C  604 = MALHEUR
C  607 = OCHOCO
C  614 = UMATILLA
C  616 = WALLOWA-WHITMAN
C  619 = WHITMAN (MAPPED TO WALLOWA-WHITMAN)
C----------
C  VARIABLE DECLARATIONS:
C----------
C
C
      INTEGER I,NUMFOR
C
      INTEGER JFOR(7),KFOR(7)
C
C----------
C  DATA STATEMENTS:
C
C  SPECIES ORDER:
C   1=WP,  2=WL,  3=DF,  4=GF,  5=MH,  6=WJ,  7=LP,  8=ES,
C   9=AF, 10=PP, 11=WB, 12=LM, 13=PY, 14=YC, 15=AS, 16=CW,
C  17=OS, 18=OH
C
C  SPECIES EXPANSION:
C  WJ USES SO JU (ORIGINALLY FROM UT VARIANT; REALLY PP FROM CR VARIANT)
C  WB USES SO WB (ORIGINALLY FROM TT VARIANT)
C  LM USES UT LM
C  PY USES SO PY (ORIGINALLY FROM WC VARIANT)
C  YC USES WC YC
C  AS USES SO AS (ORIGINALLY FROM UT VARIANT)
C  CW USES SO CW (ORIGINALLY FROM WC VARIANT)
C  OS USES BM PP BARK COEFFICIENT
C  OH USES SO OH (ORIGINALLY FROM WC VARIANT)
C----------
      DATA JFOR/604,607,614,616,619, 0, 0 /, NUMFOR /5/
C
      DATA KFOR/1,1,1,1,1,1,1 /
C----------
      IF (KODFOR .EQ. 0) GOTO 30
      DO 10 I=1,NUMFOR
      IF (KODFOR .EQ. JFOR(I)) GOTO 20
   10 CONTINUE
      CALL ERRGRO (.TRUE.,3)
      WRITE(JOSTND,11) JFOR(IFOR)
   11 FORMAT(T12,'FOREST CODE USED IN THIS PROJECTION IS  ',I4)
      GOTO 30
   20 CONTINUE
      IF(I .EQ. 5)THEN
        WRITE(JOSTND,21)
   21   FORMAT(T12,'WHITMAN NF (619) BEING MAPPED TO WALLOWA-WHITMAN ',
     &  '(616) FOR FURTHER PROCESSING.')
        I=4
      ENDIF
      IFOR=I
      IGL=KFOR(I)
   30 CONTINUE
      KODFOR=JFOR(IFOR)
      RETURN
      END
