      SUBROUTINE EXPPNB
      IMPLICIT NONE
C----------
C BM $Id$
C----------
C
C     VARIANT DEPENDENT EXTERNAL REFERENCES FOR THE
C     PARALLEL PROCESSING EXTENSION.
C
      REAL BAB(9),BBA,BREL,CPCCF,CDBAL,CLBA,D,PCCFO,CCFO,BALO
      REAL BAO,XPPDDS,BX,XPPMLT
      REAL RDANUW
C
C
C     CALLED TO COMPUTE THE DDS MODIFIER THAT ACCOUNTS FOR THE DENSITY
C     OF NEIGHBORING STANDS (CALLED FROM DGF).
C
      ENTRY PPDGF (XPPDDS,BAO,BALO,CCFO,PCCFO,D,CLBA,CDBAL,CPCCF,
     &             BREL,BBA)
C----------
C  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
C----------
      RDANUW = XPPDDS
      RDANUW = BAO
      RDANUW = BALO
      RDANUW = CCFO
      RDANUW = PCCFO
      RDANUW = D
      RDANUW = CLBA
      RDANUW = CDBAL
      RDANUW = CPCCF
      RDANUW = BREL
      RDANUW = BBA
C
      RETURN
C
C     CALLED TO COMPUTE THE REGENT MULTIPLIER THAT ACCOUNTS FOR
C     THE DENSITY OF NEIGHBORING STANDS (CALLED FROM REGENT).
C
      ENTRY PPREGT (XPPMLT,BX,BAB,CCFO)
C----------
C  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
C----------
      RDANUW = XPPMLT
      RDANUW = BX
      RDANUW = BAB(1)
      RDANUW = CCFO
C
      RETURN
      END
