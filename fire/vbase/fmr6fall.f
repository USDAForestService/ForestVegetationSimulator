*          17 = grand / white fir
*          18 = misc. hardwoods
*          19 = populus
*          20 = alders
*
*     PNWMD,WCWMD,SOWMD,BMWMD,ECWMD - arrays with mapping from plant 
*     association to soil moisture (1 = wet, 2 = mesic, 3 = dry).
*     these may be different than the moisture mapping used for snag decay.
*
*     PNOTSH,WCOTSH,SOOTSH,BMOTSH,ECOTSH - arrays with mapping from plant
*     association to soil depth (1 = other, 2 = shallow)
*
*     PNOTRT,WCOTRT,SOOTRT,BMOTRT,ECOTRT - arrays with mapping from plant
*     association to slope position (1 = other, 2 = ridgetop)
*
*     SML -  size code (1 = small, 2 = medium, 3 = large)
